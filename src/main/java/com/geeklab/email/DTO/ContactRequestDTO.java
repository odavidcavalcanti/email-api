package com.geeklab.email.DTO;

import jakarta.validation.constraints.Email;
import jakarta.validation.constraints.NotBlank;
import jakarta.validation.constraints.Size;

public record ContactRequestDTO(

        @NotBlank
        @Size(max = 80)
        String name,

        @NotBlank
        @Email
        String email,

        @NotBlank
        String messageText
) {
}
